package util;

/**
 *
 * @author GAMING PC
 */
public class Symbols {
    public int id;
    public String name;
    public String type;
    public Object value;
    public int row;
    public int column;
    
    public Symbols(int id, String name, String type, Object value, int row, int column){
        this.id = id;
        this.name = name;
        this.type = type;
        this.value = value;
        this.row = row;
        this.column = column;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}
